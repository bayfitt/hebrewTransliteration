function removeVowels () {
  let inputTextRV = $('#inputRemove').val();
  outputText = inputTextRV.replace(/[\u0591-\u05F4, \uFB1D-\uFB4F]/gu, i => hebCharsRV[i]);
  $('#outputRemove').val(outputText);
}

function removeCantillation () {
  let inputTextRV = $('#inputRemove').val();
  outputText = inputTextRV.replace(/[\u0591-\u05F4, \uFB1D-\uFB4F]/gu, i => hebCharsRC[i]);
  $('#outputRemove').val(outputText);
}

$("#input_button").click(function(){
  const radioBtn = $('input[name=removeRadioBtn]:checked').val()
  if (radioBtn === undefined) {
    alert("Please Select An Option")
  } else if (radioBtn === "cantillationVowels") {
    removeVowels();
  } else {
    removeCantillation();
  }
});
